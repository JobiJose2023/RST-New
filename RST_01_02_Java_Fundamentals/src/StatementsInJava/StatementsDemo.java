package StatementsInJava;

public class StatementsDemo {
	
	

	public static void main(String[] args) {
		
		
		// Declaration statements
		int count;
		
		// Expression statements
		count = 4;
		
		// Control-flow statements
		
		if(count <10) {
			System.out.println("Hello everyone...");
		}
		
	}

}
