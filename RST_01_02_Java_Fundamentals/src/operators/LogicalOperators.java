package operators;

public class LogicalOperators {

	public static void main(String[] args) {
		

		boolean a =true;
		boolean b = false;
		
		
		System.out.println(" --------------------------------------------");
		System.out.println(" --------------LOGICAL OPERATORS----------");
		System.out.println(" --------------------------------------------");
		
		System.out.println("a && b = "+ (a&&b)); // 
		System.out.println("a || b = "+ (a||b)); // 
		System.out.println(" !(a && b) = "+ !(a && b)); // 
	
		
		System.out.println(" --------------------------------------------");
		System.out.println(" --------------------------------------------");
	}

}
