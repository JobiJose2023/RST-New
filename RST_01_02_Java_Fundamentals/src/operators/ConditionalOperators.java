package operators;

public class ConditionalOperators {

	public static void main(String[] args) {
		
		System.out.println(" --------------------------------------------");
		System.out.println(" --------------CONDITIONAL OPERATORS----------");
		System.out.println(" --------------------------------------------");
		
		
		int a,b;
		a =10;
		
		 b = (a == 1) ? 100 : 200;
		System.out.println(" b = "+ b); // 200
		
		
		
		 b = (a == 10) ? 100 : 200;
		
		System.out.println(" b = "+ b); // 100
		
		
		
		System.out.println(" --------------------------------------------");
		System.out.println(" --------------------------------------------");
	}

}
